package com.jsp.employee_employee_lsptop_jdbc_crud.dto;

public class Laptop {
	private int laptopSerialNumber;
	private  String laptopName;
	private String laptopMemory;
	private double laptopPrice;
	public int getLaptopSerialNumber() {
		return laptopSerialNumber;
	}
	public void setLaptopSerialNumber(int laptopSerialNumber) {
		this.laptopSerialNumber = laptopSerialNumber;
	}
	public String getLaptopName() {
		return laptopName;
	}
	public void setLaptopName(String laptopName) {
		this.laptopName = laptopName;
	}
	public String getLaptopMemory() {
		return laptopMemory;
	}
	public void setLaptopMemory(String laptopMemory) {
		this.laptopMemory = laptopMemory;
	}
	public double getLaptopPrice() {
		return laptopPrice;
	}
	public void setLaptopPrice(double laptopPrice) {
		this.laptopPrice = laptopPrice;
	}
	

}
